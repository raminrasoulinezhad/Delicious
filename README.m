# Screen
  
  
  screen            ---> to creat 
  screan ~r <name>  ---> to reconnect
  screan ~rls       ---> to list the screens
  
  cntr + AD         ---> temp close
  cntr + AK         ---> kill
  
  cntr + AN         ---> next screen
  cntr + AS         ---> splite
  
  
